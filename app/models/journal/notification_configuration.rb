class Journal::NotificationConfiguration
  class << self
    attr_reader :active

    def reset
      self.active = true
    end

    def with(send_notifications, &block)
      old_value = active

      self.active = send_notifications

      block.call
    ensure
      self.active = old_value
    end

    def with_reset(&block)
      block.call
    ensure
      reset
    end

    protected

    attr_writer :active
  end

  self.active = true
end
